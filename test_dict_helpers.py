from dict_helpers import sort_headwords


def test_sort_headwords():
    scrambled_chars = [
        "k",
        "l",
        "a",
        "ą",
        "i",
        "b",
        "h",
        "j",
        "c",
        "ę",
        "f",
        "g",
        "ć",
        "d",
        "e",
        "ł",
        "m",
        "n",
        "ż",
        "ń",
        "y",
        "z",
        "ś",
        "t",
        "u",
        "v",
        "ó",
        "p",
        "q",
        "r",
        "s",
        "w",
        "x",
        "ź",
        "o",
    ]
    expected_order = [
        "a",
        "ą",
        "b",
        "c",
        "ć",
        "d",
        "e",
        "ę",
        "f",
        "g",
        "h",
        "i",
        "j",
        "k",
        "l",
        "ł",
        "m",
        "n",
        "ń",
        "o",
        "ó",
        "p",
        "q",
        "r",
        "s",
        "ś",
        "t",
        "u",
        "v",
        "w",
        "x",
        "y",
        "z",
        "ź",
        "ż"
    ]
    assert sort_headwords(scrambled_chars) == expected_order
